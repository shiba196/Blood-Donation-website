<h2>Voluntary Non-remunerated Blood Donor</h2>
<font size="5px">A person who gives blood, plasma or cellular components of his or her own free will and receives no payment, either in the form of cash or in kind which is considered a substitute for money. This would include time off work other than that reasonably needed for donation and travel. Small tokens, refreshments and reimbursements of direct travel costs are compatible with voluntary, non-remunerated donation.</font>

<h2>Autologous Blood Donor</h2>
<font size="5px">A patient who donates his/her blood to be stored, in case there is a future need for infusion for himself/herself. Here the patient himself/herelf acts as the blood donor. This may be done prior to an elective surgery.</font>

<h2>Apheresis Donor</h2>
<font size="5px">A donor who donates only one of his/her blood components through the process of cell separation. This person may be either voluntary or a replacement donor. For instance, some blood banks can separate platelets and a donor might be able to donate platelets more frequently than they donate blood.</font>
